## Python output
# Use the print() function as many times as you want. 
# Each call prints text on a new line by default.
## Examples
print('Hello World')
print('Hello again')

## double quote
# Text in Python must be inside quotes. 
# You can use either " double quotes or ' single quotes.
## Examples
print('Hello World')
print("Hello again")

## Printing new line
# By default, the print() function ends with a new line.
# If you want to print multiple words on the same line, you can use the end parameter.
## Examples
print("Hello World!", end=" ")
print("I will print on the same line.")

## Print numbers
# You can also use the print() function to display numbers.
# However, unlike text, we don't put numbers inside double quotes.
## Examples
print(3)
print(358)
print(50000)

# Also do math inside the print() function.
## Emaples
print(3 + 3)
print(2 * 5)

## Print mix texts and numbers
# combine text and numbers in one output by separating them with a comma.
## Emamples 
print("I am", 35, "years old.")

